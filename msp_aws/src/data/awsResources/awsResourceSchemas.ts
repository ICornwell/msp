import { createSchema } from 'msp_common';

export const eksClusterSchema = createSchema('eksCluster')
  .withId('eksCluster', '1.0')
  .withProperty('clusterName')
  .forType<string>()
  .withDictionaryId('aws-eks-clusterName', '1.0')
  .withInfoType('Text')
  .withDefaultLabel('Cluster Name')
  .endProperty()
  .withProperty('region')
  .forType<string>()
  .withDictionaryId('aws-eks-region', '1.0')
  .withInfoType('Text')
  .withDefaultLabel('Region')
  .endProperty()
  .withProperty('status')
  .forType<string>()
  .withDictionaryId('aws-eks-status', '1.0')
  .withInfoType('Text')
  .withDefaultLabel('Status')
  .endProperty()
  .withProperty('version')
  .forType<string>()
  .withDictionaryId('aws-eks-version', '1.0')
  .withInfoType('Text')
  .withDefaultLabel('Version')
  .endProperty()
  .buildSchema();

