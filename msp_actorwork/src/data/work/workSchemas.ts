import {createSchema} from 'msp_common'

export const workSchema = createSchema('work')
  .withId('work', '1.0')
  .withProperty('workreference')
    .forType<string>()
    .withDictionaryId('workActor-work-workreference', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Work Reference')
    .endProperty()
  .withProperty('name')
    .forType<string>()
    .withDictionaryId('workActor-work-name', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Name')
    .endProperty()
  .withProperty('type')
    .forType<string>()
    .withDictionaryId('workActor-work-type', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Type')
    .endProperty()
  .withProperty('description')
    .forType<string>()
    .withDictionaryId('workActor-work-description', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Description')
    .endProperty()
 .withProperty('raisedOn')
    .forType<Date>()
    .withDictionaryId('workActor-work-raisedOn', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('Raised On')
    .endProperty()
 .withProperty('slaDueDate')
    .forType<Date>()
    .withDictionaryId('workActor-work-slaDueDate', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('SLA Due Date')
    .endProperty()
 .withProperty('deadline')
    .forType<Date>()
    .withDictionaryId('workActor-work-deadline', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('Deadline')
    .endProperty()
  .buildSchema();