import {createSchema} from 'msp_common'

export const participationSchema = createSchema('participation')
  .withId('participation', '1.0')
  .withProperty('name')
    .forType<string>()
    .withDictionaryId('workActor-participation-name', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Name')
    .endProperty()
  .withProperty('type')
    .forType<string>()
    .withDictionaryId('workActor-participation-type', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Type')
    .endProperty()
  .withProperty('description')
    .forType<string>()
    .withDictionaryId('workActor-participation-description', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Description')
    .endProperty()
 .withProperty('createdOn')
    .forType<Date>()
    .withDictionaryId('workActor-participation-createdOn', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('Created On')
    .endProperty()
 .withProperty('slaDueDate')
    .forType<Date>()
    .withDictionaryId('workActor-participation-slaDueDate', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('SLA Due Date')
    .endProperty()
 .withProperty('deadline')
    .forType<Date>()
    .withDictionaryId('workActor-participation-deadline', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('Deadline')
    .endProperty()
  .buildSchema();