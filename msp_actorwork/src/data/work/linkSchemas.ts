import {createSchema} from 'msp_common'

export const linkSchema = createSchema('link')
  .withFQId({ namespace: 'actorWork', version: '1.0'})
  .withProperty('name')
    .forType<string>()
    .withDictionaryId('workActor-link-name', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Name')
    .endProperty()
  .withProperty('type')
    .forType<string>()
    .withDictionaryId('workActor-link-type', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Type')
    .endProperty()
  .withProperty('description')
    .forType<string>()
    .withDictionaryId('workActor-link-description', '1.0')
    .withInfoType('Text')
    .withDefaultLabel('Description')
    .endProperty()
 .withProperty('createdOn')
    .forType<Date>()
    .withDictionaryId('workActor-link-createdOn', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('Created On')
    .endProperty()
 .withProperty('slaDueDate')
    .forType<Date>()
    .withDictionaryId('workActor-link-slaDueDate', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('SLA Due Date')
    .endProperty()
 .withProperty('deadline')
    .forType<Date>()
    .withDictionaryId('workActor-link-deadline', '1.0')
    .withInfoType('Date')
    .withDefaultLabel('Deadline')
    .endProperty()
  .buildSchema();